    // ── Step 3: Follow relationships ──────────────────────────────────
    private int createFollows(List<Long> userIds) {
        Random rng = new Random(99);
        int count = 0;
        int n = userIds.size();
        // Each user follows ~30-50 others, ensuring dense mutual follows
        for (int i = 0; i < n; i++) {
            long follower = userIds.get(i);
            Set<Integer> targets = new LinkedHashSet<>();
            // Deterministic neighborhood follows (users nearby in the list)
            for (int offset = 1; offset <= 25 && targets.size() < 25; offset++) {
                targets.add((i + offset) % n);
            }
            // Random additional follows
            while (targets.size() < 45) {
                int t = rng.nextInt(n);
                if (t != i) targets.add(t);
            }
            for (int t : targets) {
                try {
                    jdbc.update("""
                            INSERT INTO user_follow (follower_user_id, following_user_id, created_at)
                            VALUES (?, ?, ?)
                            """, follower, userIds.get(t), randomTimestamp(rng, 180));
                    count++;
                } catch (Exception ignored) {}
            }
        }
        return count;
    }

