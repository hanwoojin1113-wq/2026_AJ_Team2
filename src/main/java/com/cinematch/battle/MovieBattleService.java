        KeyHolder keyHolder = new GeneratedKeyHolder();
        jdbcTemplate.update(connection -> {
            PreparedStatement ps = connection.prepareStatement("""
                    INSERT INTO battle_comment (battle_id, user_id, content)
                    VALUES (?, ?, ?)
                    """, Statement.RETURN_GENERATED_KEYS);
            ps.setLong(1, battleId);
            ps.setLong(2, userId);
            ps.setString(3, normalizedContent);
            return ps;
        }, keyHolder);

        Long commentId = keyHolder.getKey() == null ? null : keyHolder.getKey().longValue();
        Map<String, Object> result = new LinkedHashMap<>();
        result.put("comment", fetchComment(commentId, userId));
