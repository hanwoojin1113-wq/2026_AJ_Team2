    public List<Map<String, Object>> getActiveBattles(Long currentUserId) {
        initTables();
        List<Map<String, Object>> battles = jdbcTemplate.query("""
                SELECT b.id, b.creator_user_id, b.battle_type, b.battle_label,
                       b.option_a_label, b.option_b_label, b.option_a_image_url, b.option_b_image_url,
                       b.vote_a, b.vote_b, b.created_at,
                       u.login_id AS creator_login_id,
                       u.nickname AS creator_nickname,
                       ma.id AS movie_a_id, ma.movie_cd AS movie_a_code,
                       COALESCE(ma.title, ma.movie_name) AS movie_a_title,
                       ma.poster_image_url AS movie_a_poster,
                       COALESCE(ma.vote_average, 0) AS movie_a_rating,
                       mb.id AS movie_b_id, mb.movie_cd AS movie_b_code,
                       COALESCE(mb.title, mb.movie_name) AS movie_b_title,
                       mb.poster_image_url AS movie_b_poster,
                       COALESCE(mb.vote_average, 0) AS movie_b_rating,
                       bv.voted_option AS my_vote
                FROM movie_battle b
                JOIN movie ma ON ma.id = b.movie_a_id
                LEFT JOIN movie mb ON mb.id = b.movie_b_id
                LEFT JOIN "USER" u ON u.id = b.creator_user_id
                LEFT JOIN battle_vote bv ON bv.battle_id = b.id AND bv.user_id = ?
                ORDER BY b.created_at DESC
                LIMIT 20
                """, (rs, rowNum) -> toBattleRow(rs), currentUserId == null ? -1L : currentUserId);
        for (Map<String, Object> battle : battles) {
            Object id = battle.get("id");
            if (id instanceof Number n) {
                Map<String, Object> topComment = fetchTopComment(n.longValue());
                battle.put("topComment", topComment);
                battle.put("hasTopComment", !topComment.isEmpty());
            }
        }
        return battles;
    }

