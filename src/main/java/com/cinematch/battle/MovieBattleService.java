    public Map<String, Object> shareBattle(Long battleId, Long senderUserId, Long receiverUserId) {
        initTables();
        requireBattle(battleId);
        if (senderUserId == null || receiverUserId == null || senderUserId.equals(receiverUserId)) {
            throw new ResponseStatusException(BAD_REQUEST, "怨듭쑀 ??곸씠 ?щ컮瑜댁???딆뒿?덈떎.");
        }
        Integer mutual = jdbcTemplate.queryForObject("""
                SELECT COUNT(*)
                FROM user_follow f1
                JOIN user_follow f2
                  ON f2.follower_user_id = f1.following_user_id
                 AND f2.following_user_id = f1.follower_user_id
                WHERE f1.follower_user_id = ?
                  AND f1.following_user_id = ?
                """, Integer.class, senderUserId, receiverUserId);
        if (mutual == null || mutual == 0) {
            throw new ResponseStatusException(BAD_REQUEST, "Only mutual followers can receive shared battles.");
        }
        jdbcTemplate.update("""
                INSERT INTO battle_share (battle_id, sender_user_id, receiver_user_id)
                VALUES (?, ?, ?)
                """, battleId, senderUserId, receiverUserId);

        Map<String, Object> result = new LinkedHashMap<>();
        result.put("shared", true);
        result.put("battleId", battleId);
        result.put("receiverUserId", receiverUserId);
        return result;
    }

    private void incrementVote(Long battleId, String option) {
        jdbcTemplate.update("UPDATE movie_battle SET " + voteColumn(option) + " = " + voteColumn(option) + " + 1 WHERE id = ?", battleId);
    }

    private void decrementVote(Long battleId, String option) {
        jdbcTemplate.update("UPDATE movie_battle SET " + voteColumn(option) + " = GREATEST(0, " + voteColumn(option) + " - 1) WHERE id = ?", battleId);
    }

    private String voteColumn(String option) {
        return "A".equals(option) ? "vote_a" : "vote_b";
    }

