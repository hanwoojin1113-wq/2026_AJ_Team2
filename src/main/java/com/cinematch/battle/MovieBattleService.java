    public Map<String, Object> createBattle(String title,
                                            Long movieAId,
                                            Long movieBId,
                                            String optionALabel,
                                            String optionBLabel,
                                            String optionAImageUrl,
                                            String optionBImageUrl,
                                            Long creatorUserId) {
        initTables();
        String normalizedTitle = requireText(title, "배틀 타이틀을 입력해주세요.", 200);
        String normalizedOptionA = requireText(optionALabel, "첫 번째 선택지 이름을 입력해주세요.", 100);
        String normalizedOptionB = requireText(optionBLabel, "두 번째 선택지 이름을 입력해주세요.", 100);
        String normalizedImageA = requireText(optionAImageUrl, "첫 번째 선택지 이미지를 입력해주세요.", 500);
        String normalizedImageB = requireText(optionBImageUrl, "두 번째 선택지 이미지를 입력해주세요.", 500);

        if (movieAId == null) {
            throw new ResponseStatusException(BAD_REQUEST, "영화를 하나 이상 선택해주세요.");
        }
        if (movieBId != null && movieAId.equals(movieBId)) {
            throw new ResponseStatusException(BAD_REQUEST, "서로 다른 영화를 선택해주세요.");
        }
        requireMovie(movieAId);
        if (movieBId != null) {
            requireMovie(movieBId);
        }

        KeyHolder keyHolder = new GeneratedKeyHolder();
        jdbcTemplate.update(connection -> {
            PreparedStatement ps = connection.prepareStatement("""
                    INSERT INTO movie_battle (
                        creator_user_id, movie_a_id, movie_b_id, battle_type, battle_label,
                        option_a_label, option_b_label, option_a_image_url, option_b_image_url
                    )
                    VALUES (?, ?, ?, 'USER', ?, ?, ?, ?, ?)
                    """, Statement.RETURN_GENERATED_KEYS);
            ps.setLong(1, creatorUserId);
            ps.setLong(2, movieAId);
            if (movieBId == null) {
                ps.setObject(3, null);
            } else {
                ps.setLong(3, movieBId);
            }
            ps.setString(4, normalizedTitle);
            ps.setString(5, normalizedOptionA);
            ps.setString(6, normalizedOptionB);
            ps.setString(7, normalizedImageA);
            ps.setString(8, normalizedImageB);
            return ps;
        }, keyHolder);

        Map<String, Object> result = new LinkedHashMap<>();
        result.put("id", keyHolder.getKey() == null ? null : keyHolder.getKey().longValue());
        result.put("created", true);
        return result;
    }

    public Map<String, Object> vote(Long battleId, String option, Long userId) {
        initTables();
        String normalizedOption = option == null ? "" : option.trim().toUpperCase();
        if (!normalizedOption.equals("A") && !normalizedOption.equals("B")) {
            throw new ResponseStatusException(BAD_REQUEST, "선택지는 A 또는 B여야 합니다.");
        }

        Integer exists = jdbcTemplate.queryForObject("SELECT COUNT(*) FROM movie_battle WHERE id = ?", Integer.class, battleId);
        if (exists == null || exists == 0) {
            throw new ResponseStatusException(NOT_FOUND, "배틀을 찾을 수 없습니다.");
        }

        String prevVote = jdbcTemplate.query(
                "SELECT voted_option FROM battle_vote WHERE battle_id = ? AND user_id = ?",
                rs -> rs.next() ? rs.getString("voted_option") : null,
                battleId, userId);

        if (prevVote != null) {
            jdbcTemplate.update("DELETE FROM battle_vote WHERE battle_id = ? AND user_id = ?", battleId, userId);
            decrementVote(battleId, prevVote);
            if (prevVote.equals(normalizedOption)) {
                return fetchResult(battleId, null);
            }
        }

        jdbcTemplate.update(
                "INSERT INTO battle_vote (battle_id, user_id, voted_option) VALUES (?, ?, ?)",
                battleId, userId, normalizedOption);
        incrementVote(battleId, normalizedOption);
        return fetchResult(battleId, normalizedOption);
    }

